package com.xworkz.Footwear;

public class Footwear {
	
	int id;
	String size;
	String name;
	String color;
	String model;
	String brandName;
	
	Footwear(int i,String s,String n,String c,String m,String b){
		
		this.id=i;
		this.size=s;
		this.name=n;
		this.color=c;
		this.model=m;
		this.brandName=b;
		
	}

}
